/**
 * Find pair index by from and to currency symbols
 * @param from - Source currency symbol (e.g., "BTC", "ETH", "EUR")
 * @param to - Target currency symbol (defaults to "USD")
 * @returns pair index if found, throws error if not found
 */
export function findPairIndex(from: string, to: string = "USD"): number {
  const pair = pairs.find(
    (p) =>
      p.from_.toUpperCase() === from.toUpperCase() &&
      p.to.toUpperCase() === to.toUpperCase()
  );

  if (!pair) {
    throw new Error(`Pair ${from}/${to} not found in available pairs`);
  }

  return pair.pair_index;
}

/**
 * Get all available trading pairs
 * @returns array of {from, to, pairIndex} objects
 */
export function getAvailablePairs(): Array<{
  from: string;
  to: string;
  pairIndex: number;
  type: string;
}> {
  return pairs.map((pair) => ({
    from: pair.from_,
    to: pair.to,
    pairIndex: pair.pair_index,
    type: pair.type,
  }));
}
